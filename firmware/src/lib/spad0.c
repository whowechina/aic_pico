#define _N_TABLES 8
#define _ITER_ADD 5

void rotate_right(uint8_t* data, int n_bytes, int n_bits)
{
	uint8_t prior = data[n_bytes - 1];
	for (int i = 0; i < n_bytes; i++) {
		uint8_t current = data[i];
		data[i] = (current >> n_bits) | ((prior & ((1 << n_bits) - 1)) << (8 - n_bits));
		prior = current;
	}
}

void spad0_decrypt(const uint8_t* input, uint8_t* output, size_t len)
{
	for (size_t i = 0; i < len; i++) {
		output[i] = _SPAD0_INV_TABLE[_N_TABLES][input[i]];
	}
	int count = ((output[15] >> 4) + 7);
	int table = output[15] + _ITER_ADD * count;
	for (int iter = 0; iter < count; iter++) {
		table -= _ITER_ADD;
		rotate_right(output, 15, 5);
		for (int i = 0; i < 15; i++) {
			output[i] = _SPAD0_INV_TABLE[table % _N_TABLES][output[i]];
		}
	}
}
